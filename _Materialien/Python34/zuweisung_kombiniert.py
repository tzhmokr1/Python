# Zahlen-Operatoren
x = 12
print(x)

x += 3      # Erhoehen von x
print(x)

x -= 9      # Vermindern von x
print(x)

x **= 2     # Quadrieren von x
print(x)

x *= 3      # Verdreifachen von x
print(x)

x //= 7     # ganzzahliges Teilen von x
print(x)

x /= 4      # Teilen von x
print(x)

x %= 2      # Dividieren, Rest berechnen
print(x)

# Zeichenketten-Operatoren
t = " hallo"
print(t)

t += " python"   # Anhaengen an t
print(t)

t *= 3      # Verdreifachen von t
print(t)
